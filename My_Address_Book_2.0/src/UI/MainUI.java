    public boolean checkFields(JTextField j1, JTextField j2, JTextField j3, JTextField j4) {

        String warning = "",
                name = j1.getText(),
                mobile = j2.getText(),
                landline = j3.getText(),
                email = j4.getText();

        if (!name.contains(",")) {
            warning += "Please enter a valid name \n";
            JOptionPane.showMessageDialog(rootPane, warning);

            return false;
        }
        if (mobile.length() != 11 && mobile.length() != 0) {
            warning += "Please enter a valid mobile number \n";

            JOptionPane.showMessageDialog(rootPane, mobile.length());
            JOptionPane.showMessageDialog(rootPane, warning);

            return false;

        }

        if (landline.length() != 9 && landline.length() != 0) {
            warning += "Please enter a valid landline number \n";
            JOptionPane.showMessageDialog(rootPane, warning);

            return false;

        }
        if (!email.contains("@") && !email.contains(".com")) {
            warning += "Please enter a valid email address \n";
            JOptionPane.showMessageDialog(rootPane, warning);

            return false;

        }




        return true;
    }

