    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (searchNameBar.getSelectedItem().toString().equalsIgnoreCase("whole")) {
            reloadBar(resultNameBar, Main.Driver.addressBook.searchName(searchNameField.getText()).toArray());
        } else if (searchNameBar.getSelectedItem().toString().equalsIgnoreCase("last")) {
            reloadBar(resultNameBar, Main.Driver.addressBook.searchLastName(searchNameField.getText()).toArray());
        } else if (searchNameBar.getSelectedItem().toString().equalsIgnoreCase("first")) {
            reloadBar(resultNameBar, Main.Driver.addressBook.searchFirstName(searchNameField.getText()).toArray());
        } else if (searchNameBar.getSelectedItem().toString().equalsIgnoreCase("mobile")) {
            reloadBar(resultNameBar, Main.Driver.addressBook.searchMobile(searchNameField.getText()).toArray());
        } else if (searchNameBar.getSelectedItem().toString().equalsIgnoreCase("landline")) {
            reloadBar(resultNameBar, Main.Driver.addressBook.searchLandline(searchNameField.getText()).toArray());
        } else if (searchNameBar.getSelectedItem().toString().equalsIgnoreCase("email")) {
            reloadBar(resultNameBar, Main.Driver.addressBook.searchEmail(searchNameField.getText()).toArray());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

